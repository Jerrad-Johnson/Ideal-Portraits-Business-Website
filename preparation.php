        <center><h1 class="headline">PREPARING FOR YOUR PHOTOSHOOT</h1></center>
        <span class="subheadline">I'd like all my clients to get the best pictures they can! To help with that, please use as much of this guide as you'd like. I know it's long, and if you want to skip to the most important parts - just read the sections "makeup" and "clothing."</span>
        <br /><hr />


        <span class="headline2" id="changingclothing"><h3 class="ip">CHANGING CLOTHING</h3></span>
        <span>Change as many times as you'd like. However, if we're outdoors - it may be difficult to find a place to change. </span>

        <hr />

        <span class="headline2" id="clothing"><h3 class="ip">CLOTHING</h3></span>
        <span>I encourage you to bring a variety of clothing, but try to prevent wrinkles. Also, avoid strong colors - they make you look washed out.
        <br /><br />Pieces that fit loosely and hang away from you rarely look nice.
        <br /><br />Check your clothing for frays, wrinkles, hair, dandruff, stains, and damage.</span>
        <hr />

        <span class="headline2" id="eyeglasses"><h3 class="ip">EYE GLASSES AND CONTACTS</h3></span>
        <span>If you want to wear glasses, please bring every pair you have! Also, to avoid glare, you could buy a frame without lenses. But, the picture may seem less authentic.
        <br /><br />If the center of your eye is near the top of your frame, consider having them adjusted. And if your glasses tend to slip down, you can get nose pads that hold better. Although, if you use bifocals - this advice may not work.
        <br /><br />If you wear contact lenses, they'll show - at least in headshots. To see how that'll appear, look at my "headshot" gallery and click or tap the pictures. You'll be able to see who's wearing contact lenses.</span>
     
        <hr />

        <span class="headline2" id="friends"><h3 class="ip">FRIENDS / FAMILY</h3></span>
        <span>If you bring a friend, you'll feel more relaxed - and that'll show in your pictures!</span>

        <hr />

        <span class="headline2" id="haircare"><h3 class="ip">HAIR CARE</h3></span>
        <span>My hair is very short, and I don't yet have much advice to give. I can offer the basics: use shampoo, condiitoner, volumizer. And if your color is fading, re-dye it.
        <br /><br />If you have dandruff, consider using your treatment, bringing a lint roller, or wearing lighter clothing.</span>

        <hr />

        <span class="headline2" id="hairstyle"><h3 class="ip">HAIR STYLE</h3></span>
        <span>Determine your face's shape, and then find hairstyles that match it - there are guides online. Also, ladies: if you have a tall forehead, consider wearing bangs.  </span>

        <hr />

        <span class="headline2" id="ideas"><h3 class="ip">IDEAS</h3></span>
        <span >If you have ideas you’d like to try, please share! You can search (flickr or google) for other's shoots, and look for anything (poses, styles/themes, etc) that strikes you or that matches your personality – to make the shoot more “you.” </span>

        <hr />

        <span class="headline" id="makeup"><h3 class="ip">MAKEUP</h3></span>
        <span>There are two common reasons that your face will appear shiny, and to avoid that: please wash your face with gentle soap before your shoot, to remove natural oils. However, do this only if it won't irritate your face. Second, please use a finishing powder.
        <br /><br />If you use a highlighter, use natural colors. Don't use, for example, blue/green - it'll appear silver.
        <br /><br />Most important: <b>keep your makeup smooth.</b> Ensure there are no blotches, because this can be very difficult to edit. Also, your foundation should match your skin color and be blended to avoid lines.
        <br /><br />When it comes to eyeshadow and lipstick, it depends on the style of portrait we're doing. They're great for glamour, but usually not for a corporate headshot. Your skin also matters - if you're very pale, they can add life - but be careful, so that you don't appear more pale.
        <br /><br />The best option is to have your makeup done professionally. If you're short on funds, Headlines Academy is inexpensive. I can't vouch for their work, though - I'm not acquainted with it.
		<br /><br />Please bring your makeup with you, <b>especially your finishing powder.</b>
        <br /><br /><b>Men</b>, note this: If you don't wear makeup, still wash your face - but be sure your face won't appear irritated.
        </span>
        
        <hr />

        <span class="headline2" id="music"><h3 class="ip">MUSIC</h3></span>
        <span>I play pop music to help everyone feel relaxed, but if you'd like a different genre (or artist) - please ask!</span>

        <hr />

        <span class="headline2" id="otheritems"><h3 class="ip">OTHER ITEMS</h3></span>
        <span>Bring your <b>hair spray</b>, a brush (or comb), lint roller, hair ties/clips, lip moisturizer/gloss, and if we're taking pictures of your baby - wipes.</span>

        <hr />

        <span class="headline2" id="piercings"><h3 class="ip">PIERCINGS</h3></span>
        <span>Piercing jewelry is usually not photogenic, so I suggest removing it - unless the uncovered mark is worse. I especially recommend not wearing lip or nose jewelry. Earrings are usually safe, but sometimes they align with the light, making a strong reflection.
        <br /><br />However, if your jewelry is important to you - keep it in!</span>
     
        <hr />
        
        <span class="headline2" id="pimples"><h3 class="ip">PIMPLES</h3></span>
        <span>Avoid the things that cause you to break out. Refined sugar, oils, chocolate that might worsen your acne, your monthly cycle (if you can schedule around it), or stress (I know, we all wish we could avoid that!). Remember that picking can make things worse.
        </span>

        <hr />

        <span class="headline2" id="props"><h3 class="ip">PROPS</h3></span>
        <span>I encourage you to bring props. If you’re a musician, bring your favorite instrument. If you’re a recent graduate, bring your framed diploma. Jewelry works well, too!
        <br /><br />However, please don't bring anything you want to be in the picture that may be offensive, such as: pieces related to politics, religion, sex, sexual-orientation, drugs, or gangs. </span>
     
        <hr />

        <span class="headline2" id="shaving"><h3 class="ip">SHAVING</h3></span>
        <span>Plan when to shave, especially if you have sensitive skin; avoid marks, but also avoid stubble. 
        <br /><br />If your skin is sensitive, consider a double-edge razor rather than a disposable head or electric razor; they can be harsh and irritate skin. Changing from canned shaving cream to a shaving soap bar may help. Also, consider using noxzema to soften your hair before shaving.</span>
     
        <hr />

        <span class="headline2" id="skin"><h3 class="ip">SKIN</h3></span>
        <span>Wherever your skin will be visible, be using a lotion - such as CeraVe. Don't wear any lotion during our shoot, though. And if a moisturizer will cause acne or folliculitis (etc), don't use it. 
        <br /><br />For a couple of weeks before our shoot, use a <b>lip moisturizer</b> to prevent cracked lips.
        </span>

        <hr />         

        <span class="headline2" id="skinmarks"><h3 class="ip">SKIN MARKS</h3></span>
        <span>For a few hours before our shoot, avoid wearing tight pieces wherever skin might be visible. If you wear a watch - keep it on during the shoot, or only wear it in our last pictures. 
        <br /><br />You might have "dermatographia." If you scratch yourself - that area may turn red and stay red for hours. It's more common in teens and young adults. Be careful about rubbing or scratching your skin - especially during the shoot. Try to plan your outfits in order, so that one on won't leave visible marks when you wear the next. This is most often a problem with western hats.
        </span>

        <hr />        



        <span class="headline2" id="suits"><h3 class="ip">SUITS AND DRESS CLOTHING</h3></span>
        <span>Be careful that your clothing doesn't get wrinkled during transport! 
        <br /><br />Choose a tie whose width is compatible with your lapels.
        <br /><br />The most common problem is a jacket that doesn't fit correctly. You'll see bumps in the shoulders, the front pockets as low as your belt, the sleeves will extend to your knuckles, or you're thin and your jacket isn't brought in at the waist.
        <br /><br />If your suit needs tailoring and you can't get it done, decide if you'd prefer pictures without the jacket.
        <br /><br />And if you decide to get a new suit, I strongly recommend using a guide to learn how they should fit - rather than relying on the salesman.
        <br /><br />Shirt stays are very helpful for keeping your shirt tucked. Also consider metal collar stays (if your shirt allows), tie clips, and handkerchiefs.
        </span>
        

        


